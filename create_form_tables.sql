# MongoDB Setup Instructions for Forms Module

Since this project uses MongoDB exclusively, no SQL tables are needed.

The following MongoDB collections will be created automatically:
- form_links
- health_insurance_forms
- insurance_recommendations
- term_insurance_forms (future)
- child_education_forms (future)
- child_wedding_forms (future)
- financial_planning_forms (future)
- customer_forms (future)

The insurance recommendations data will be automatically populated when the service is first initialized.